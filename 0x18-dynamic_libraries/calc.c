#include <stdio.h>

/**
 * add - add two number
 * @a: first number
 * @b: second number
 * Return: result
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - minus two number
 * @a: first number
 * @b: second number
 * Return: result
 */

int sub(int a, int b)
{
        return (a - b);
}

/**
 * mul - multiply two number
 * @a: first number
 * @b: second number
 * Return: result
 */

int mul(int a, int b)
{       
        return (a * b);
}

/**
 * div - divide two number
 * @a: first number
 * @b: second number
 * Return: result
 */

int div(int a, int b)
{
        return (a / b);
}

/**
 * mod - modulus two number
 * @a: first number
 * @b: second number
 * Return: result
 */

int mod(int a, int b)
{
        return (a % b);
}
